class increment{
    public static void main(String[] args)
    {
        int x=10;
        int a=x++;
        System.out.println("x:"+x);
        System.out.println("a: "+a);

        a=++x;
        System.out.println("X: "+x);
        System.out.println("A :"+a);

        System.out.println();
        System.out.println("END the operation: ");
    }
}