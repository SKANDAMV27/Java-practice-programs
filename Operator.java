class Operator
{
        public static void main(String[] args)
        {
    int a=90;
    int b=90;
    int result=a+b;
    System.out.println("ADDITION: "+result);
    result=a-b;
    System.out.println("SUBSTRACTION: "+result);
    result=a*b;
    System.out.println("MULTIPLACATION: "+result);
    result=a/b;
    System.out.println("DIVISION: "+result);
}
}